function      [mue,dmuex,dDenex,duex]=Finte(i,dx,DENe,Ue,Te)


if i==2  %  1 order

     mu=1.789*10^(-5)*(Te(i)/288)^1.5*(288+110.4)/(Te(i)+110.4);
     mue=mu;
     mu1=1.789*10^(-5)*(Te(i-1)/288)^1.5*(288+110.4)/(Te(i-1)+110.4);
     dmuex=(mu-mu1)/dx;
     
     dDenex=(DENe(i)-DENe(i-1))/dx;
    
     duex=(Ue(i)-Ue(i-1))/dx;
     
end 

if i==3  %  2 order
    
     mu=1.789*10^(-5)*(Te(i)/288)^1.5*(288+110.4)/(Te(i)+110.4);
     mue=mu;
     mu1=1.789*10^(-5)*(Te(i-1)/288)^1.5*(288+110.4)/(Te(i-1)+110.4);
     mu2=1.789*10^(-5)*(Te(i-2)/288)^1.5*(288+110.4)/(Te(i-2)+110.4);
     dmuex=(3/2*mu-4/2*mu1+1/2*mu2)/dx;
     
     dDenex=(3/2*DENe(i)-4/2*DENe(i-1)+1/2*DENe(i-2))/dx;
     
     duex=(3/2*Ue(i)-4/2*Ue(i-1)+1/2*Ue(i-2))/dx;
    
    
end
%

if i==4  %  3 order
    
     mu=1.789*10^(-5)*(Te(i)/288)^1.5*(288+110.4)/(Te(i)+110.4);
     mue=mu;
     mu1=1.789*10^(-5)*(Te(i-1)/288)^1.5*(288+110.4)/(Te(i-1)+110.4);
     mu2=1.789*10^(-5)*(Te(i-2)/288)^1.5*(288+110.4)/(Te(i-2)+110.4);
     mu3=1.789*10^(-5)*(Te(i-3)/288)^1.5*(288+110.4)/(Te(i-3)+110.4);
     dmuex=(11/6*mu-18/6*mu1+9/6*mu2-2/6*mu3)/dx;
     
     dDenex=(11/6*DENe(i)-18/6*DENe(i-1)+9/6*DENe(i-2)-2/6*DENe(i-3))/dx;
     
     duex=(11/6*Ue(i)-18/6*Ue(i-1)+9/6*Ue(i-2)-2/6*Ue(i-3))/dx;
    
    
  
end

% 
% 
% 
if i==5  %  4 order
    
     mu=1.789*10^(-5)*(Te(i)/288)^1.5*(288+110.4)/(Te(i)+110.4);
     mue=mu;
     mu1=1.789*10^(-5)*(Te(i-1)/288)^1.5*(288+110.4)/(Te(i-1)+110.4);
     mu2=1.789*10^(-5)*(Te(i-2)/288)^1.5*(288+110.4)/(Te(i-2)+110.4);
     mu3=1.789*10^(-5)*(Te(i-3)/288)^1.5*(288+110.4)/(Te(i-3)+110.4);
     mu4=1.789*10^(-5)*(Te(i-4)/288)^1.5*(288+110.4)/(Te(i-4)+110.4);
     dmuex=(50/24*mu-96/24*mu1+72/24*mu2-32/24*mu3+6/24*mu4)/dx;
     
     dDenex=(50/24*DENe(i)-96/24*DENe(i-1)+72/24*DENe(i-2)-32/24*DENe(i-3)+6/24*DENe(i-4) )/dx;
     
     duex=(50/24*Ue(i)-96/24*Ue(i-1)+72/24*Ue(i-2)-32/24*Ue(i-3)+6/24*Ue(i-4) )/dx;
    
    
  
end

if i>=6  %  5 order
    
     mu=1.789*10^(-5)*(Te(i)/288)^1.5*(288+110.4)/(Te(i)+110.4);
     mue=mu;
     mu1=1.789*10^(-5)*(Te(i-1)/288)^1.5*(288+110.4)/(Te(i-1)+110.4);
     mu2=1.789*10^(-5)*(Te(i-2)/288)^1.5*(288+110.4)/(Te(i-2)+110.4);
     mu3=1.789*10^(-5)*(Te(i-3)/288)^1.5*(288+110.4)/(Te(i-3)+110.4);
     mu4=1.789*10^(-5)*(Te(i-4)/288)^1.5*(288+110.4)/(Te(i-4)+110.4);
     mu5=1.789*10^(-5)*(Te(i-5)/288)^1.5*(288+110.4)/(Te(i-5)+110.4);
     dmuex=(274/120*mu-600/120*mu1+600/120*mu2-400/120*mu3+150/120*mu4-24/120*mu5)/dx;
     
     dDenex=(274/120*DENe(i)-600/120*DENe(i-1)+600/120*DENe(i-2)-400/120*DENe(i-3)+150/120*DENe(i-4)-24/120*DENe(i-5) )/dx;
     
     duex=(274/120*Ue(i)-600/120*Ue(i-1)+600/120*Ue(i-2)-400/120*Ue(i-3)+150/120*Ue(i-4)-24/120*Ue(i-5) )/dx;
    
    
  
end